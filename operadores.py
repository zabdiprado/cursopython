# operadores aritméticos

print(10 + 8) # suma
print(23 - 7) # resta
print(8 * 5) # repetición
print(15 / 2) # división
print(11 % 4) # modulo
print(11 // 4) # division de piso
print(2 ** 3) # poteniación

#operadores de cadena de texto

print("Hola" + "mundo") # concatenación
print("Hola" * 3) # repetición
print("Hola" + "mundo" * 3)

# operadores de comparación

print("Hola" == "hola") # igual que
print("Hola" != "hola") # distinto que
print(3 < 11) # mayor que, menor que
print(11 >= 10) # mayor o igual
print(10 <= 10) # menor o igual

# operadores de existencias

print("ola" in "hola")
print("ola" not in "hola")